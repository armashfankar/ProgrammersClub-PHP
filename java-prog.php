	<h3><b><font color="red">INTRODUCTION TO JAVA</font></b></h3>
		 <p class="MB0">Java is a computer programming language that is concurrent, class-based, object-oriented, and specifically designed to have as few implementation dependencies as possible. It is intended to let application developers "write once, run anywhere" (WORA), meaning that code that runs on one platform does not need to be recompiled to run on another. Java applications are typically compiled to bytecode (class file) that can run on any Java virtual machine (JVM) regardless of computer architecture.</p><br>


<h3><b><font color="red">HISTORY OF JAVA</font></b></h3><br>
<img src="images/java_founder.jpg" height="300" wodth="300"><br><br>
<p class="MB0">Java is a programming language created by James Gosling from Sun Microsystems (Sun) in 1991. The first publicly available version of Java (Java 1.0) was released in 1995.

Sun Microsystems was acquired by the Oracle Corporation in 2010. Oracle has now the steermanship for Java.

Over time new enhanced versions of Java have been released. The current version of Java is Java 1.7 which is also known as Java 7. </p> <br><br>



		 <font color="red"><b>What is JAVA?</b></font><br>
<br>
		 <font color="black">1-Platform independent:</font> Java programs use the Java virtual machine as abstraction and do not access the operating system directly. This makes Java programs highly portable. A Java program (which is standard complaint and follows certain rules) can run unmodified on all supported platforms, e.g. Windows or Linux.<br><br>

 <font color="black">2-Object-orientated programming language:</font> Except the primitive data types, all elements in Java are objects.
<br><br>
 <font color="black">3-Strongly-typed programming language: </font>Java is strongly-typed, e.g. the types of the used variables must be pre-defined and conversion to other objects is relatively strict, e.g. must be done in most cases by the programmer.

